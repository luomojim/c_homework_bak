#define _CRT_SECURE_NO_WARNINGS
//陶陶家的院子里有一棵苹果树，每到秋天树上就会结出 10 个苹果。苹果成熟的时候，陶陶就会跑去摘苹果。
//陶陶有个 30 厘米高的板凳，当她不能直接用手摘到苹果的时候，就会踩到板凳上再试试。
//
//现在已知 10 个苹果到地面的高度，以及陶陶把手伸直的时候能够达到的最大高度，请帮陶陶算一下她能够摘到的苹果的数目。
//假设她碰到苹果，苹果就会掉下来。
//
//输入格式
//输入包括两行数据。第一行包含 10 个 100 到 200 之间（包括 100 和 200）的整数（以厘米为单位）
//分别表示 10 个苹果到地面的高度，两个相邻的整数之间用一个空格隔开。第二行只包括一个 100 到 120 之间（包含 100 和 120）的整数（以厘米为单位），
//表示陶陶把手伸直的时候能够达到的最大高度。
//
//输出格式
//输出包括一行，这一行只包含一个整数，表示陶陶能够摘到的苹果的数目。

#include <stdio.h>
#include <math.h>

int pick_apple()
{
	int heigh[11] = { 0 };
	int max = 0;
	int count = 0;
	for (int i = 0; i < 10; i++)
	{
		scanf("%d",&heigh[i]);
	}
	scanf("%d", &max);
	max = max + 30;
	for (int i = 0; i < 10; i++)
	{
		heigh[i] = heigh[i] - max;
		if (heigh[i] <= 0)
		{
			count++;
		}
	}
	printf("%d\n", count);
	return 0;
}